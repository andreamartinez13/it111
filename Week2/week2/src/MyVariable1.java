public class MyVariable1 {

    public static void main(String[] args) {
        String name = "Andrea"; // After a statement do not forget semicolon ;
        int hours = 40; // we are assigning 40 (number to hours variable. No quotes on int variables (or numbers)
        System.out.println(name);
        System.out.println(hours);

        String country, city, street;
        country = "Europe";
        city = "Mexico";
        street = "Boren Av";

        System.out.println(country);
        System.out.println(city);
        System.out.println(street);

        int weeks, days, month;
        weeks = 4;
        days = 365;
        month = 12;





    }

}
